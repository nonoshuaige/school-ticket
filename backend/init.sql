SET NAMES utf8mb4;

-- ============================================================
-- 清理旧表（先删子表再删父表，避免外键错误）
-- ============================================================
DROP TABLE IF EXISTS `note_like`;
DROP TABLE IF EXISTS `user_note_comment`;
DROP TABLE IF EXISTS `user_follow`;
DROP TABLE IF EXISTS `order`;
DROP TABLE IF EXISTS `ticket_category`;
DROP TABLE IF EXISTS `user_note`;
DROP TABLE IF EXISTS `event`;
DROP TABLE IF EXISTS `user`;

-- ============================================================
-- 用户表
-- ============================================================
CREATE TABLE `user` (
  `user_id`      BIGINT       NOT NULL AUTO_INCREMENT COMMENT '用户ID',
  `phone`        VARCHAR(20)  NOT NULL                COMMENT '手机号（登录账号）',
  `password`     VARCHAR(255) NOT NULL                COMMENT '密码（BCrypt加密）',
  `nickname`     VARCHAR(50)  DEFAULT ''              COMMENT '昵称',
  `create_time`  DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `update_time`  DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`user_id`),
  UNIQUE KEY `uk_phone` (`phone`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户表';

-- ============================================================
-- 活动表
-- ============================================================
CREATE TABLE `event` (
  `event_id`        BIGINT       NOT NULL AUTO_INCREMENT COMMENT '活动ID',
  `title`           VARCHAR(100) NOT NULL                COMMENT '活动标题',
  `description`     TEXT                                 COMMENT '活动描述',
  `organizer`       VARCHAR(100) NOT NULL                COMMENT '主办方',
  `venue`           VARCHAR(100) NOT NULL                COMMENT '场地',
  `poster_url`      VARCHAR(255)                         COMMENT '海报URL',
  `sale_start_time` DATETIME     NOT NULL                COMMENT '开售时间',
  `sale_end_time`   DATETIME     NOT NULL                COMMENT '停售时间',
  `event_start_time` DATETIME    NOT NULL                COMMENT '活动开始时间',
  `event_end_time`  DATETIME     NOT NULL                COMMENT '活动结束时间',
  `status`          TINYINT      NOT NULL DEFAULT 0      COMMENT '0=未发布 1=售票中 2=已结束 3=已下架',
  `create_time`     DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `update_time`     DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`event_id`),
  KEY `idx_status` (`status`),
  KEY `idx_sale_time` (`sale_start_time`, `sale_end_time`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='活动表';

-- ============================================================
-- 票档表
-- ============================================================
CREATE TABLE `ticket_category` (
  `ticket_id`          BIGINT        NOT NULL AUTO_INCREMENT COMMENT '票类ID',
  `event_id`           BIGINT        NOT NULL                COMMENT '所属活动',
  `name`               VARCHAR(50)   NOT NULL                COMMENT '票档名',
  `price`              DECIMAL(10,2) NOT NULL                COMMENT '单价',
  `total_quantity`     INT           NOT NULL                COMMENT '总库存',
  `remaining_quantity` INT           NOT NULL                COMMENT '剩余可售数',
  `description`        VARCHAR(255)                          COMMENT '票档说明',
  `create_time`        DATETIME      NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `update_time`        DATETIME      NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`ticket_id`),
  KEY `idx_event_id` (`event_id`),
  CONSTRAINT `fk_ticket_event` FOREIGN KEY (`event_id`) REFERENCES `event` (`event_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='票档表';

-- ============================================================
-- 订单表
-- ============================================================
CREATE TABLE `order` (
  `order_no`      VARCHAR(64)   NOT NULL                COMMENT '订单号（主键）',
  `user_id`       BIGINT        NOT NULL                COMMENT '用户ID',
  `ticket_id`     BIGINT        NOT NULL                COMMENT '票档ID',
  `quantity`      INT           NOT NULL DEFAULT 1      COMMENT '购买张数',
  `total_price`   DECIMAL(10,2) NOT NULL                COMMENT '总价',
  `status`        TINYINT       NOT NULL DEFAULT 0      COMMENT '0=待支付 1=已支付 2=已取消 3=已退款 4=已核销',
  `expire_time`   DATETIME      NOT NULL                COMMENT '支付截止时间',
  `create_time`   DATETIME      NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '下单时间',
  `paid_time`     DATETIME                               COMMENT '支付时间',
  `cancel_time`   DATETIME                               COMMENT '取消时间',
  `refund_time`   DATETIME                               COMMENT '退款时间',
  `use_time`      DATETIME                               COMMENT '核销时间',
  `update_time`   DATETIME      NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`order_no`),
  KEY `idx_user_status` (`user_id`, `status`),
  CONSTRAINT `fk_order_user` FOREIGN KEY (`user_id`) REFERENCES `user` (`user_id`),
  CONSTRAINT `fk_order_ticket` FOREIGN KEY (`ticket_id`) REFERENCES `ticket_category` (`ticket_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='订单表';

-- ============================================================
-- 关注关系表
-- ============================================================
CREATE TABLE `user_follow` (
  `id`           BIGINT   NOT NULL AUTO_INCREMENT COMMENT '自增ID',
  `follower_id`  BIGINT   NOT NULL               COMMENT '关注者用户ID',
  `user_id`      BIGINT   NOT NULL               COMMENT '被关注者用户ID',
  `create_time`  DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '关注时间',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_follow` (`follower_id`, `user_id`),
  KEY `idx_follower` (`follower_id`),
  KEY `idx_user_id` (`user_id`),
  CONSTRAINT `fk_follow_follower` FOREIGN KEY (`follower_id`) REFERENCES `user` (`user_id`),
  CONSTRAINT `fk_follow_user` FOREIGN KEY (`user_id`) REFERENCES `user` (`user_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户关注关系表';

-- ============================================================
-- 用户笔记表
-- ============================================================
CREATE TABLE `user_note` (
  `note_id`     BIGINT       NOT NULL AUTO_INCREMENT COMMENT '笔记ID',
  `user_id`     BIGINT       NOT NULL               COMMENT '作者用户ID',
  `content`     VARCHAR(500) NOT NULL               COMMENT '笔记内容',
  `create_time` DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  PRIMARY KEY (`note_id`),
  KEY `idx_user_id` (`user_id`),
  CONSTRAINT `fk_note_user` FOREIGN KEY (`user_id`) REFERENCES `user` (`user_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户笔记表';

-- ============================================================
-- 笔记点赞表
-- ============================================================
CREATE TABLE `note_like` (
  `id`          BIGINT   NOT NULL AUTO_INCREMENT COMMENT '自增ID',
  `note_id`     BIGINT   NOT NULL               COMMENT '笔记ID',
  `user_id`     BIGINT   NOT NULL               COMMENT '点赞用户ID',
  `create_time` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '点赞时间',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_note_user` (`note_id`, `user_id`),
  KEY `idx_note_id` (`note_id`),
  CONSTRAINT `fk_like_note` FOREIGN KEY (`note_id`) REFERENCES `user_note` (`note_id`),
  CONSTRAINT `fk_like_user` FOREIGN KEY (`user_id`) REFERENCES `user` (`user_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='笔记点赞表';

-- ============================================================
-- 笔记评论表（二级展平）
-- ============================================================
CREATE TABLE `user_note_comment` (
  `comment_id`    BIGINT       NOT NULL AUTO_INCREMENT COMMENT '评论ID',
  `note_id`       BIGINT       NOT NULL               COMMENT '归属笔记ID',
  `user_id`       BIGINT       NOT NULL               COMMENT '评论发布者ID',
  `content`       VARCHAR(500) NOT NULL               COMMENT '评论内容',
  `root_id`       BIGINT       DEFAULT 0              COMMENT '根评论ID，0表示一级评论',
  `parent_id`     BIGINT       DEFAULT 0              COMMENT '直接回复的父评论ID',
  `reply_to_uid`  BIGINT       DEFAULT NULL           COMMENT '被回复的用户ID',
  `is_deleted`    TINYINT      DEFAULT 0              COMMENT '逻辑删除 0=正常 1=已删除',
  `create_time`   DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `update_time`   DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`comment_id`),
  KEY `idx_note_id` (`note_id`),
  KEY `idx_root_id` (`root_id`),
  CONSTRAINT `fk_comment_note` FOREIGN KEY (`note_id`) REFERENCES `user_note` (`note_id`),
  CONSTRAINT `fk_comment_user` FOREIGN KEY (`user_id`) REFERENCES `user` (`user_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='笔记评论表';

